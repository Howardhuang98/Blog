class Solution:
    def solveNQueens(self, n: int) -> list:
        def generateBoard():
            board = list()
            for i in range(n):
                row[queens[i]] = "Q"
                board.append("".join(row))
                row[queens[i]] = "."
            return board

        def backtrack(row: int):
            if row == n:
                board = generateBoard()
                solutions.append(board)
            else:
                for i in range(n):
                    if i in columns or row - i in diagonal1 or row + i in diagonal2:
                        continue # 如果i不能下就跳过此次循环
                    queens[row] = i
                    columns.add(i) # 第i列被占
                    diagonal1.add(row - i) #对角
                    diagonal2.add(row + i)#对角
                    backtrack(row + 1) # 考虑下一行
                    columns.remove(i)
                    diagonal1.remove(row - i)
                    diagonal2.remove(row + i)

        solutions = list()
        queens = [-1] * n
        columns = set()
        diagonal1 = set()
        diagonal2 = set()
        row = ["."] * n
        backtrack(0)
        return solutions

if __name__ == '__main__':
    solu = Solution()
    a = solu.solveNQueens(4)
